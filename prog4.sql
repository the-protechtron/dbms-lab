-- -----------------------------
-- 3. Queries and Views
-- -----------------------------

-- Query 1: Fetch students from semester 4, section C
SELECT S.*, SS.SEM, SS.SEC
FROM STUDENT S
JOIN CLASS C ON S.USN = C.USN
JOIN SEMSEC SS ON C.SSID = SS.SSID
WHERE SS.SEM = 4 AND SS.SEC = 'C';

-- Query 2: Count students by gender for each SEMSEC
SELECT SS.SEM, SS.SEC, S.GENDER, COUNT(*) AS COUNT
FROM STUDENT S
JOIN CLASS C ON S.USN = C.USN
JOIN SEMSEC SS ON C.SSID = SS.SSID
GROUP BY SS.SEM, SS.SEC, S.GENDER
ORDER BY SS.SEM;

-- Query 3: Create a view for Test 1 marks of a specific student
CREATE VIEW STU_TEST1_MARKS_VIEW AS
SELECT USN, SUBCODE, TEST1
FROM IAMARKS
WHERE USN = '1RN13CS091';

-- Query 4: Simulated procedure to calculate average IA marks
-- Use this update manually to simulate a stored procedure
UPDATE IAMARKS
SET FINALIA = (TEST1 + TEST2 + TEST3 - LEAST(TEST1, TEST2, TEST3)) / 2.0;

-- Query 5: Categorizing students based on IA marks (only for SEM = 8)
SELECT S.USN, S.SNAME, S.ADDRESS, S.PHONE, S.GENDER,
    CASE
        WHEN IA.FINALIA BETWEEN 17 AND 20 THEN 'OUTSTANDING'
        WHEN IA.FINALIA BETWEEN 12 AND 16 THEN 'AVERAGE'
        ELSE 'WEAK'
    END AS CAT
FROM STUDENT S
JOIN IAMARKS IA ON S.USN = IA.USN
JOIN SUBJECT SUB ON IA.SUBCODE = SUB.SUBCODE
WHERE SUB.SEM = 8;
